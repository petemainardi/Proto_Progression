/**
 *  Set the health value of a HealthTracker to 0 on contact.
 */
// ================================================================================================
// ||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
// ================================================================================================
[RequireComponent(typeof(Collider))]
public class InstaKill : MonoBehaviour
{

    private void OnCollisionEnter(Collision collision)
    {
        HealthTracker health = collision.gameObject.GetComponentInParent<HealthTracker>();
        if (health != null)
        {
            health.Health.Value = 0;
        }
    }

}
